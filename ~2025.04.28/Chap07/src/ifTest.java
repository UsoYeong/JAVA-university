
public class ifTest {

	public static void main(String[] args) {
		
		int i = 20;
		
		if(i == 10) {
			System.out.println("i는 10입니다.");
		}else if(i == 20) {
			System.out.println("i는 20입니다.");
		}else {
			System.out.println("i는 10도 20도 아닙니다");
		}
		
		System.out.println("여기는 프로그램의 끝입니다.");
	}

}
